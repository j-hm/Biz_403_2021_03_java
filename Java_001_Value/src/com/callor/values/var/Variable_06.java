package com.callor.values.var;

public class Variable_06 {
	public static void main(String[] args) {
		
		
		// 정수형 변수선언과 클리어
		int num1 = 0;
		int num2 = 0;
		
		// int로 선언된 변수는 정수값만 저장할 수 있다
		// 최대 -2의 31승 ~ 2의 31승 -1까지의 범위만 저장할 수 있다
		num1 = 30;
		num2 = 30;
		
		// -2의 63승 ~ 2의 63승 -1까지 저장가능
		long numLong1 = 30L;
	//(키워드)(변수명)=(값)
		
		float num3 = 30.0F;
		
		double num4 = 30.0;
		
	}

}
